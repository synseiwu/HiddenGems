export function getDefaultPageSection(pageKey, sectionKey) {
  return defaultPageContent?.[pageKey]?.sections?.[sectionKey] || {}
}

export function getDefaultPageSections(pageKey) {
  return defaultPageContent?.[pageKey]?.sections || {}
}

export function pageContentToRows() {
  return Object.entries(defaultPageContent).flatMap(([page_key, page]) =>
    Object.entries(page.sections || {}).map(([section_key, section], index) => ({
      page_key,
      page_label: page.label,
      section_key,
      sort_order: index + 1,
      content_type: section.body ? 'rich_text' : 'hero',
      title: section.title || '',
      subtitle: section.subtitle || '',
      eyebrow: section.eyebrow || '',
      body: section.body || '',
      button_text: section.button_text || '',
      active: true
    }))
  )
}
